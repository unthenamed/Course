package main

import "fmt"

func main() {
	fmt.Println("Perbandingan")

	a := 9
	b := 3

	// a samadengn b ?
	fmt.Println(a == b)

	// a tidak samadengan b ?
	fmt.Println(a != b)

	// a kurangdari b ?
	fmt.Println(a < b)

	// a kurang dari atau  samadengan b ?
	fmt.Println(a <= b)

	// a lebih dari b ?
	fmt.Println(a > b)

	// a lebih dari atau samadengan b ?
	fmt.Println(a >= b)

}
